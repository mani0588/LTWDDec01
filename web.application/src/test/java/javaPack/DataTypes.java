package javaPack;

public class DataTypes {

	public static void main(String[] args) {

		System.out.println("Selenium webdriver with Java...");

		// Data-types - 9
		// Numerical
		byte by = 1; // 2^8
		short sh = 10;// 2^16
		int in = 10;// 2^32
		long lon = 10; // 2^64

		// Numerical with decimals
		float fl = 10.759827330947f; // 6 points
		double dl = 10.759827330947; // 12 points

		// chars
		char ch = '@'; // any single char
		String str = "anything with any length - a-z 0-9 %$^&*()&^%#$%#";

		// logical dicisions
		boolean bool = true; // true / false

		System.out.println(by);
		System.out.println(sh);
		System.out.println(in);
		System.out.println(lon);
		System.out.println(fl);
		System.out.println(dl);
		System.out.println(ch);
		System.out.println(str);
		System.out.println(bool);
		
	}

}
